    private void printButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printButtonActionPerformed
        boolean addTicket = teller.addTicket((String) concertComboBox.getSelectedItem(),
                (String) tierComboBox.getSelectedItem(), 
                (String) dateComboBox.getSelectedItem(), 
                (((String) seatComboBox.getSelectedItem()).matches("-?\\d+(\\.\\d+)?")?(String) seatComboBox.getSelectedItem():"0"), 
                nameTextField.getText(), 
                TYPES.get((String) typeComboBox.getSelectedItem()));
        if(addTicket){
            String ticket = teller.getTicket(nameTextField.getText(), 
                    (String) concertComboBox.getSelectedItem(), 
                    (String) dateComboBox.getSelectedItem());
            System.out.println("Ticket : "+ticket);
            String[] t = ticket.split(":");
            infoTextArea.setText(
                     "Conncert Title : "+t[1]+"\n"
                    +"Concert Hall : "+t[2]+"\n"
                    +"Name : "+t[0]+"\n"
                    +"Tier : "+t[3]+"\n"
                    +"Seat : "+(t[4].equals("0")?"Not Numbered":t[4])+"\n"
                    +"Price : "+(t[6].equals("0")?t[5]:(Float.parseFloat(t[5])-(Float.parseFloat(t[5])*Float.parseFloat(t[6]))))+"  "
                    +"Discount : "+(Float.parseFloat(t[6])*100)+"%\n"
                    +"Date : "+t[7]+"  "
                    +"Time : "+t[8]+":"+t[9]);
        }
    }//GEN-LAST:event_printButtonActionPerformed

