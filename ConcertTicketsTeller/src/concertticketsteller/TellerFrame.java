    private void concertComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_concertComboBoxActionPerformed
        seatComboBox.setEnabled(true);
        dateComboBox.setModel(new javax.swing.DefaultComboBoxModel<>(teller.getDates((String) concertComboBox.getSelectedItem())));
        tierComboBox.setModel(new javax.swing.DefaultComboBoxModel<>(teller.getTiers((String) concertComboBox.getSelectedItem())));
        seatComboBox.setModel(new javax.swing.DefaultComboBoxModel<>(teller.getSeats((String) concertComboBox.getSelectedItem(), (String) tierComboBox.getSelectedItem(), (String) dateComboBox.getSelectedItem())));
        if(seatComboBox.getItemAt(0).equals("FULL")){
            seatComboBox.setEnabled(false);
            infoTextArea.setText("");
            infoTextArea.setText("Full please choose another");
        }
    }//GEN-LAST:event_concertComboBoxActionPerformed

